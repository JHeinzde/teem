#!/usr/bin/env python3
"""Visualise whether Hamming-weight groups of the input byte produce
distinguishably different power traces.

The trace index i (0–254) is used directly as the input byte.
The emulator measures Hamming Distance of each memory write, so for the
SubBytes step the relevant intermediate value is:

    HD(AddRoundKey output, SubBytes output)
    = HW((i ^ key[0]) ^ sbox[i ^ key[0]])

Since key[0] = 0 in dpa_traces.s this simplifies to HW(i ^ sbox[i]).
Each trace is therefore assigned to the group HW(i ^ sbox[i]) ∈ {0…8}.

Two-panel figure
----------------
Top   : every individual trace, coloured by its HW group (low alpha).
Bottom: per-group mean ± 1 std-dev band — this is where similarity /
        dissimilarity between HW groups becomes visible.
"""

import glob

import numpy as np
import matplotlib.pyplot as plt
import matplotlib.cm as cm
from matplotlib.lines import Line2D

SBOX = [
    0x63,0x7c,0x77,0x7b,0xf2,0x6b,0x6f,0xc5,0x30,0x01,0x67,0x2b,0xfe,0xd7,0xab,0x76,
    0xca,0x82,0xc9,0x7d,0xfa,0x59,0x47,0xf0,0xad,0xd4,0xa2,0xaf,0x9c,0xa4,0x72,0xc0,
    0xb7,0xfd,0x93,0x26,0x36,0x3f,0xf7,0xcc,0x34,0xa5,0xe5,0xf1,0x71,0xd8,0x31,0x15,
    0x04,0xc7,0x23,0xc3,0x18,0x96,0x05,0x9a,0x07,0x12,0x80,0xe2,0xeb,0x27,0xb2,0x75,
    0x09,0x83,0x2c,0x1a,0x1b,0x6e,0x5a,0xa0,0x52,0x3b,0xd6,0xb3,0x29,0xe3,0x2f,0x84,
    0x53,0xd1,0x00,0xed,0x20,0xfc,0xb1,0x5b,0x6a,0xcb,0xbe,0x39,0x4a,0x4c,0x58,0xcf,
    0xd0,0xef,0xaa,0xfb,0x43,0x4d,0x33,0x85,0x45,0xf9,0x02,0x7f,0x50,0x3c,0x9f,0xa8,
    0x51,0xa3,0x40,0x8f,0x92,0x9d,0x38,0xf5,0xbc,0xb6,0xda,0x21,0x10,0xff,0xf3,0xd2,
    0xcd,0x0c,0x13,0xec,0x5f,0x97,0x44,0x17,0xc4,0xa7,0x7e,0x3d,0x64,0x5d,0x19,0x73,
    0x60,0x81,0x4f,0xdc,0x22,0x2a,0x90,0x88,0x46,0xee,0xb8,0x14,0xde,0x5e,0x0b,0xdb,
    0xe0,0x32,0x3a,0x0a,0x49,0x06,0x24,0x5c,0xc2,0xd3,0xac,0x62,0x91,0x95,0xe4,0x79,
    0xe7,0xc8,0x37,0x6d,0x8d,0xd5,0x4e,0xa9,0x6c,0x56,0xf4,0xea,0x65,0x7a,0xae,0x08,
    0xba,0x78,0x25,0x2e,0x1c,0xa6,0xb4,0xc6,0xe8,0xdd,0x74,0x1f,0x4b,0xbd,0x8b,0x8a,
    0x70,0x3e,0xb5,0x66,0x48,0x03,0xf6,0x0e,0x61,0x35,0x57,0xb9,0x86,0xc1,0x1d,0x9e,
    0xe1,0xf8,0x98,0x11,0x69,0xd9,0x8e,0x94,0x9b,0x1e,0x87,0xe9,0xce,0x55,0x28,0xdf,
    0x8c,0xa1,0x89,0x0d,0xbf,0xe6,0x42,0x68,0x41,0x99,0x2d,0x0f,0xb0,0x54,0xbb,0x16,
]

# ── Load traces ────────────────────────────────────────────────────────────────
trace_files = sorted(
    glob.glob("trace-*.txt"),
    key=lambda f: int(f.split("-")[1].split(".")[0]),
)
n = len(trace_files)
print(f"Loading {n} traces…")

raw = [np.loadtxt(f) for f in trace_files]
min_len = min(len(t) for t in raw)
traces = np.array([t[:min_len] for t in raw])   # shape (n, samples)
samples = np.arange(min_len)

# ── Hamming-weight groups ──────────────────────────────────────────────────────
# Correct intermediate value: HW((i ^ key[0]) ^ sbox[i ^ key[0]])
# key[0] = 0, so: HW(i ^ sbox[i])
hw_of = np.array([bin(i ^ SBOX[i]).count("1") for i in range(n)])
hw_values = sorted(set(hw_of))                             # 0 … 8

# Colour palette: one colour per HW value
palette = cm.plasma(np.linspace(0.05, 0.95, len(hw_values)))
hw_color = {hw: palette[k] for k, hw in enumerate(hw_values)}

# ── Figure ─────────────────────────────────────────────────────────────────────
fig, (ax_raw, ax_mean) = plt.subplots(2, 1, figsize=(14, 9), sharex=True)
fig.suptitle("Power traces grouped by HW(i ^ sbox[i])  —  the correct HD intermediate value", fontsize=13)

# ── Top panel: individual traces ───────────────────────────────────────────────
for i, trace in enumerate(traces):
    ax_raw.plot(samples, trace, color=hw_color[hw_of[i]], alpha=0.15, linewidth=0.5)

ax_raw.set_ylabel("Power value")
ax_raw.set_title("Individual traces (coloured by HW group)")
ax_raw.grid(True, alpha=0.3)

# ── Bottom panel: group mean ± std ─────────────────────────────────────────────
for hw in hw_values:
    group = traces[hw_of == hw]           # select traces in this group
    g_mean = group.mean(axis=0)
    g_std  = group.std(axis=0)
    color  = hw_color[hw]
    ax_mean.plot(samples, g_mean, color=color, linewidth=1.5, label=f"HW={hw} (n={len(group)})")
    ax_mean.fill_between(samples, g_mean - g_std, g_mean + g_std, color=color, alpha=0.15)

ax_mean.set_xlabel("Sample index")
ax_mean.set_ylabel("Power value")
ax_mean.set_title("Per-group mean ± 1 std-dev")
ax_mean.grid(True, alpha=0.3)
ax_mean.legend(loc="upper right", fontsize=8, ncol=2)

# ── Shared legend for the top panel (proxy artists) ───────────────────────────
proxy = [
    Line2D([0], [0], color=hw_color[hw], linewidth=1.5, label=f"HW={hw}")
    for hw in hw_values
]
ax_raw.legend(handles=proxy, loc="upper right", fontsize=8, ncol=2)

plt.tight_layout()
output_file = "hw_groups.png"
plt.savefig(output_file, dpi=150)
print(f"Saved to {output_file}")
plt.show()
